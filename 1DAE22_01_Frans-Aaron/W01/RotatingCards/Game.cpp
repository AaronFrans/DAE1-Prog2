void Game::ClearBackground( ) const
{
	glClearColor( 0.0f, 0.0f, 0.3f, 1.0f );
	glClear( GL_COLOR_BUFFER_BIT );
}

void Game::InitRotatingCards()
{
	float baseAngle = 360.0f / Card::m_MaxRank;

	for (int i = Card::m_MinRank; i <= Card::m_MaxRank; i++)
	{
		m_RotatingCards.push_back(new Card{
			Card::Suit::diamonds,
			i,
			baseAngle * i,
			m_Circle
			});
	}
}

void Game::DrawRotatingCards() const
{

	for (Card* card : m_RotatingCards)
	{
		card->Draw(Rectf{ 0, 0, card->GetWidth() / 2, card->GetHeight() /2 });
	}
}

void Game::UpdateRotatingCards(float elapsedSec)
{
	for (Card* card : m_RotatingCards)
	{
		card->Update(elapsedSec);
	}
}

void Game::DeleteRotatingCards()
{
	for (Card* card : m_RotatingCards)
	{
		delete card;
		card = nullptr;
	}
}
